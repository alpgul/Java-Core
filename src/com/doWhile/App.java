package com.doWhile;

public class App {

	public static void main(String[] args) {

/*Java do-while Loop*/
		int i = 1;
		do {
			System.out.println(i);
			i++;
		} while (i <= 10);

/*Java Infinitive do-while Loop*/
		do

		{
			System.out.println("infinitive do while loop");
		} while (true);
	}
}
